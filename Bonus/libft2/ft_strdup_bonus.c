#include "libft_bonus.h"

char	*ft_strdup(const char *s1)
{
	char	*ptr;
	size_t	i;

	if (s1 == NULL)
		return (NULL);
	ptr = (char *)malloc(sizeof(char) * ft_strlen(s1) + 1);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
